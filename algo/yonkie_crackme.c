int32_t crc32_ghidra(int32_t namelen, uint8_t* buffer) {
	
	if (namelen <= 0) {
		return 0;
	}
	uint32_t ebp = 0;
	uint32_t edi = 0;
	uint32_t esi = 0;
	for (int i = 0; i < namelen; i++)
	{
		esi = buffer[i] ^ 0xffffffff;
		ebp ^= esi;
		esi = 8;
		do {
			edi = -(ebp & 1) & 0xedb88320;
			ebp >>= 1;
			ebp ^= edi;
			esi--;
		} while (esi != 0);
		ebp ^= 0xffffffff;
	}
	return ebp;
}

